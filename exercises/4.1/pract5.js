let estado = 'aprovada(o)';

switch (estado) {
  case 'aprovada(o)':
    console.log('Parabens, doida(o)!');
    break;

  case 'lista':
    console.log('Ta listado, meu consagrado');
    break;

  case 'reprovada':
    console.log('Força, meu bom. Estuda mais um pouco e dale');
    break;

  default:
    console.log('Não se aplica');
}